print("=== 度量衡轉換程式 ===")
print("1. 長度換算 (公里 ↔ 英里)")
print("2. 溫度換算 (攝氏 ↔ 華氏)")
print("3. 重量換算 (公斤 ↔ 磅)")

選擇 = input("請輸入選擇 (1/2/3)：")

if 選擇 == "1":
    print("\n[長度換算]")
    公里 = float(input("請輸入公里數："))
    英里 = 公里 * 0.621371
    print(公里, "公里 =", round(英里, 2), "英里")

    英里 = float(input("請輸入英里數："))
    公里 = 英里 / 0.621371
    print(英里, "英里 =", round(公里, 2), "公里")

elif 選擇 == "2":
    print("\n[溫度換算]")
    攝氏 = float(input("請輸入攝氏溫度："))
    華氏 = (攝氏 * 9/5) + 32
    print(攝氏, "°C =", round(華氏, 2), "°F")

    華氏 = float(input("請輸入華氏溫度："))
    攝氏 = (華氏 - 32) * 5/9
    print(華氏, "°F =", round(攝氏, 2), "°C")

elif 選擇 == "3":
    print("\n[重量換算]")
    公斤 = float(input("請輸入公斤數："))
    磅 = 公斤 * 2.20462
    print(公斤, "公斤 =", round(磅, 2), "磅")

    磅 = float(input("請輸入磅數："))
    公斤 = 磅 / 2.20462
    print(磅, "磅 =", round(公斤, 2), "公斤")

else:
    print("輸入錯誤，請重新執行程式！")
